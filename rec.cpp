void GetHam(float *hamming)//加窗
{
	int i;
	float a;
	a=(float)(PI*2 / (FRAMELEN - 1)); //FRAMELEN为当前语音帧的采样数
	for (i=0;i<FRAMELEN;i++)
      hamming[i] =(float)(0.54 - 0.46 * cos(a*i));//Hamming窗函数
}

void PreEmphasise (short *s, float k)//预加重
{
	int i;
	float preE;//预加重系数
	preE = k;
	for (i=FRAMELEN-1;i>=1;i--)
	{
		*(s+i) -= *(s+(i-1))*preE;//预加重后结果
	}
	*s *=(float)(1.0-preE);
}

