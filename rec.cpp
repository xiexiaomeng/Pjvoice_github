int GetZcr(short *data)//判断平均过零率or not?
{
	int pre=data[0];
	int zero=0;
	for(int i=1;i<FrmLen;++i)
	{
		if((pre>0&&data[i]<0)||pre<0&&data[i]>0)
			zero++;
		pre=data[i];
	}
	return zero;
}

