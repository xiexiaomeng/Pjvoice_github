				{
					index=i*PCEP+k;
					temp=xishu[index]-para[j].mean[k];
					temp1=temp*temp;
					temp1=temp1/(2*para[j].variance[k]);
					sum-=temp1;
					temp2/=sqrt(para[j].variance[k]);
				}
				sum=exp(sum)*temp2/sqrt(twopi);
