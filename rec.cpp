float Dtw(float *muban,int mubannum,float *data,int datanum)   //dtw算法
{
	int i,j,k;
	float dis=0.0,dis1,a;
	Hlow  hlow;


	if (mubannum>(int)(2*datanum)||datanum>(int)(mubannum*2)) 
	{
		return 100000000;
	}
	for (i=0;i<mubannum;i++)
	{
		dis1=100000;
		if (i<int(mubannum/4)) 
		{
			hlow.minnum=0;
			hlow.maxnum=int(datanum/4)+int(0.75*datanum*i/(0.75*mubannum));
		}
		else if (i>=int(mubannum/4)&&i<=int(0.75*mubannum)) 
		{
			hlow.maxnum=int(0.25*datanum+datanum*i/mubannum);
			if (hlow.maxnum>datanum)
			{
				hlow.maxnum=datanum;
			}
			hlow.minnum=int(datanum*(i-0.25*mubannum)/mubannum);
		}
		else if (i>int(0.75*mubannum&&i<=mubannum))
		{
			hlow.maxnum=datanum;
			hlow.minnum=int(datanum*(i-0.25*mubannum)/mubannum);
			if (hlow.minnum==datanum)
			{
				hlow.minnum=datanum-2;
			}
		}
		for (j=hlow.minnum;j<=hlow.maxnum;j++)
		{
			a=0.0;
			for (k=0;k<MFCCNUM;k++) 
			{
				a=a+(float)fabs(data[(j-1)*MFCCNUM+k]-muban[i*MFCCNUM+k]);
			}
			if (a<dis1)
			{
				dis1=a;
			}
		}
		dis=dis+dis1;
	}

	return dis;
}